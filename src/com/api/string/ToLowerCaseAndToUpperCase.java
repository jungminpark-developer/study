package com.api.string;

public class ToLowerCaseAndToUpperCase {

	public static void main(String[] args) {
		//221012 문자열 API-알파벳 대소문자 변경
		String word = "Park Jungmin";
		System.out.println("original: "+word);
		System.out.println("toUpperCase: "+word.toUpperCase());
		System.out.println("toLowerCase: "+word.toLowerCase());
		
	}
}
